// Equal 
console.log('3' == 3);

/*
    - coercion is the process of converting a value from one type to another
*/

// Strictly Equal
console.log(3 === 3);

console.log(3 === '3');

// Not Equal
console.log('3' != 3); 

// strict not equal 
console.log('3' !== 3);

// Greater Than
console.log(3 > 2);

// Less Than
console.log(2 < 3);

// Greater Than or Equal To
console.log(3 >= 2);

// Less Than or equal to
console.log(2 <= 2);

// And
2 && 3

// Or
2 || 3

