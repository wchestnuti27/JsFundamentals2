// Assignment Operators 
let r = 5;

// Addition Assignment
r += 1;  // r = r + 1

// Subtraction Assignment
r -= 1;  // r = r - 1

// Multiplication assignment 
r *= 1;  // r = r * 1

// Division Assignment 
r /= 1;  // r = r / 1

// remainder (or Modulus) Assignment 
r %= 1;  // r = r % 1

// Exponential Assignment 
r **= 1;  // r = r ** 1

